def drawTeapot():
    glColor3f(0,1,0)
    glRotatef(angle,0,0,1)
    #glTranslatef(1*cos(theda),0,0)
    #glTranslatef(1*cos(theda),0,0)
    glBegin(GL_LINES) 
    for fID in teapotFace:
        glVertex3fv(teapotVertex[fID[0]])
        glVertex3fv(teapotVertex[fID[1]])
        glVertex3fv(teapotVertex[fID[1]])
        glVertex3fv(teapotVertex[fID[2]])
        glVertex3fv(teapotVertex[fID[2]])
        glVertex3fv(teapotVertex[fID[0]])
    glEnd()


def display():
    glClear(GL_COLOR_BUFFER_BIT|GL_DEPTH_BUFFER_BIT)
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    glViewport(0, 0, windowWidth, windowHeight)
    glOrtho(-float(windowWidth)/2.0,float(windowWidth)/2.0,-float(windowHeight)/2.0,float(windowHeight)/2.0,-windowHeight*10.0,windowHeight*10.0)
    global theda, angle
    theda = theda + 0.1
    angle = angle + 5

    glPushMatrix()
    drawTeapot()
    glPopMatrix()
    glutSwapBuffers()
    glutPostRedisplay()

